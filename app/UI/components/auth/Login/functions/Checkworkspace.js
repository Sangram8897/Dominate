export const isworkspace_exist = _workspace => {
  return fetch(
    `https://login.dominate.ai/public/workspace/exist?workspaceId=${_workspace}`,
  )
    .then(response => response.json())
    .then(responseJson => {
      if (responseJson.exist) {
        return true;
      }
      return false;
    })
    .catch(error => {
      throw new Error(error);
    });
};
